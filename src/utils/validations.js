export function isNull(query) {
    return query === null;
}

export function isNotNull(query) {
    return query !== null;
}

export function isUndefined(query) {
    return query === undefined;
}

export function isNotUndefined(query) {
    return query !== undefined;
}

export function isDefined(query) {
    return query != null;
}

export function isNotDefined(query) {
    return query == null;
}
